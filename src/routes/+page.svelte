<h2>Theories, Speculation, and Musings</h2>

<p>Inductive and deducting reasoning, informed by Bayesian probability theory and applied to the facts you deem reliable, can provide tentative understanding.
The portion of existence that reason can possibly reach is, in my life, very small. As LaoTso put it in Chapter One of the Dao du Jing, "The Dao that can be spoken is not the eternal Dao." 

The Tao that can be told is not the eternal Tao.
The name that can be named is not the eternal name.
The nameless is the beginning of heaven and earth.
The named is the mother of ten thousand things.
Ever desireless, one can see the mystery.
Ever desiring, one can see the manifestations.
These two spring from the same source but differ in name;
     this appears as darkness.
Darkness within darkness.
The gate to all mystery.
</p>
